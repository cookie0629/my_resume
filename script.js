const observer = new IntersectionObserver(
  (entries, obs) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        obs.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.1 }
);

document.querySelectorAll('.fade-in').forEach((section) => {
  observer.observe(section);
});

// -------------------- 技能进度条动画 --------------------
const skillSection = document.getElementById('skills');
const skillProgressBars = document.querySelectorAll('.skill-progress');
let skillsAnimated = false;

function animateSkillBars() {
  skillProgressBars.forEach((bar) => {
    const percentEl =
      bar.closest('.skill-item')?.querySelector('.skill-percent');
    const targetValue = percentEl
      ? parseFloat(percentEl.textContent || '0')
      : 0;
    const target = Number.isFinite(targetValue) ? targetValue : 0;
    requestAnimationFrame(() => {
      bar.style.width = `${target}%`;
    });
  });
}

if (skillSection) {
  const skillsObserver = new IntersectionObserver(
    (entries, obs) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting && !skillsAnimated) {
          animateSkillBars();
          skillsAnimated = true;
          obs.disconnect();
        }
      });
    },
    { threshold: 0.35 }
  );
  skillsObserver.observe(skillSection);
}

// -------------------- 表单提交提示 --------------------
const contactForm = document.querySelector('.contact-form');
contactForm.addEventListener('submit', (e) => {
  e.preventDefault();
  const formData = new FormData(contactForm);
  const name = formData.get('name')?.trim();
  const currentLang = localStorage.getItem('lang') || 'zh';
  const dict = translations[currentLang] || translations.zh;
  const template = dict['contact.form.success'];
  const fallback = dict['contact.form.friend'];
  const message = template.replace('{name}', name || fallback);
  alert(message);
  contactForm.reset();
});

