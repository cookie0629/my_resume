const navLinks = document.querySelector('.nav-links');
const menuToggle = document.querySelector('.menu-toggle');
const themeToggle = document.querySelector('.theme-toggle');
const themeIcon = themeToggle.querySelector('i');
const langSelect = document.getElementById('language-select');
const root = document.body;

// -------------------- 导航与滚动交互 --------------------
menuToggle.addEventListener('click', () => {
  navLinks.classList.toggle('open');
});

document.querySelectorAll('a[href^="#"]').forEach((link) => {
  link.addEventListener('click', (e) => {
    const targetId = link.getAttribute('href');
    if (targetId.length > 1) {
      e.preventDefault();
      document.querySelector(targetId)?.scrollIntoView({ behavior: 'smooth' });
      navLinks.classList.remove('open');
    }
  });
});

// -------------------- 主题切换持久化 --------------------
const storedTheme = localStorage.getItem('theme');
if (storedTheme === 'dark') {
  root.classList.add('dark');
  themeIcon.classList.replace('bx-sun', 'bx-moon');
}

themeToggle.addEventListener('click', () => {
  root.classList.toggle('dark');
  const isDark = root.classList.contains('dark');
  themeIcon.classList.toggle('bx-sun', !isDark);
  themeIcon.classList.toggle('bx-moon', isDark);
  localStorage.setItem('theme', isDark ? 'dark' : 'light');
});

// -------------------- 多语言切换逻辑 --------------------
const textElements = document.querySelectorAll('[data-i18n]');
const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
const ariaElements = document.querySelectorAll('[data-i18n-aria-label]');

/**
 * 根据语言码更新页面所有文本/占位/aria-label
 * @param {string} language 语言缩写，如 zh/en/ru
 */
function updateContent(language) {
  const dict = translations[language] || translations.zh;

  textElements.forEach((el) => {
    const key = el.dataset.i18n;
    const text = dict[key];
    if (text !== undefined) {
      el.innerHTML = text;
    }
  });

  placeholderElements.forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    const text = dict[key];
    if (text !== undefined) {
      el.setAttribute('placeholder', text);
    }
  });

  ariaElements.forEach((el) => {
    const key = el.dataset.i18nAriaLabel;
    const text = dict[key];
    if (text !== undefined) {
      el.setAttribute('aria-label', text);
    }
  });
}

const storedLang = localStorage.getItem('lang') || 'zh';
if (langSelect) {
  langSelect.value = storedLang;
}
updateContent(storedLang);

langSelect?.addEventListener('change', (event) => {
  const selectedLang = event.target.value;
  updateContent(selectedLang);
  localStorage.setItem('lang', selectedLang);
});

// -------------------- 内容淡入动画 --------------------
const observer = new IntersectionObserver(
  (entries, obs) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        obs.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.1 }
);

